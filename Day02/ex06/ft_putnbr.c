int		ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int arr[1000];
	int i;

	i = -1;
	while (nb)
	{
		i++;
		arr[i] = nb % 10;
		nb /= 10;
	}
	while (i >= 0)
	{
		ft_putchar(arr[i] + 48);
		i--;
	}
}
