char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i])
	{
		if (!dest[i])
			return ((char *)0);
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
